    int SetHomeDirectory(String dir) 
    {
        rmi.canRun = true; // recovers from previously typed bad path
        rmi.DataHome = dir;
        dataHome.setText(dir);
        File hDir = new File(dir);
        if (!hDir.exists()) {
            if (!hDir.mkdir()) {  
                JOptionPane.showMessageDialog(null,"Cannot create data home folder");
                rmi.canRun = false;
                return 0;
            }        
        }
        return 1;
    }

